package com.learning.java.oct1;

public class Car {

    //data members

    String brand;
    String model;
    int year;
    double price;

    //Constructor
    public Car() {
    }

    //methods
    public String getBrand(){
        return this.brand;  
    }

    public String getModel() {
        return this.model;
    }

    public int getYear() {
        return this.year;
    }

    public double getPrice() {
        return this.price;
    }
}
